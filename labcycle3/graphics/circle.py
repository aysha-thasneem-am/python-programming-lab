def area_circle():
    r=int(input("Enter the radius of circle: "))
    area=3.14*r*r
    print("Area of circle with radius ",r," is: ",area)
def perimeter_circle():
    r=int(input("Enter the radius of circle: "))
    perimeter=2*3.14*r
    print("Perimeter of circle with radius ",r," is: ",perimeter)
