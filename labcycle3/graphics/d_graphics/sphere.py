def area_sphere():
    r=int(input("Enter the radius of circle: "))
    area=4*3.14*r*r
    print("Area of sphere with radius ",r," is: ",area)
def volume_sphere():
    r=int(input("Enter the radius of circle: "))
    volume=(4/3)*3.14*r*r
    print("Volume of sphere with radius ",r," is: ",volume)
